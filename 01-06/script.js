// String
let string_demo = "This is a string";
console.log('String: ',string_demo);

// Number
let number_demo = 25
console.log('Number: ',number_demo);

// Floats
let float_demo = 25.75
console.log('Float: ',float_demo);

// Booleans
let booleans_demo = true;
console.log('Boolean: ',booleans_demo);

// Null
let null_demo = null;
console.log('Null: ',null_demo);

// Undefined
let undefined_demo = undefined;
console.log('Undefined with value: ',undefined_demo);

let undefined_demo2;
console.log('Undefined without value: ',undefined_demo2);

// Objects
const objects_demo = {
  width: 250,
  height: 250,
  color: "red"
}
console.log('Objects: ',objects_demo);

// Arrays
const array_demo = ['Apple', 'Mango', 'Orange'];
console.log('Array: ',array_demo);